#include <stdio.h>

int main(void)
{
    int dan, j;

    for (dan = 1; dan <= 12; dan += 3) 
    {
        
        for (int k = 0; k < 3; k++)
        {
            printf("%d단\t\t", dan + k);
        }
        printf("\n");

        
        for (j = 1; j <= 9; j++)
        {
            for (int k = 0; k < 3; k++)
            {
                printf("%d x %d = %2d\t", dan + k, j, (dan + k) * j);
            }
            printf("\n");
        }
        printf("\n"); 
    }

    return 0;
}























































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































